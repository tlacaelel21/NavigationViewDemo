public class FeriaIntModel {

    private String nombre;
    private String ubicacion;

    public FeriaIntModel(String nombre, String ubicacion) {
        this.nombre = nombre;
        this.ubicacion = ubicacion;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public String getNombre() {
        return nombre;
    }





}
